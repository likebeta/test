class Generater(object):
    @classmethod
    def make_plist(cls, img, info):
        big_img = Image.open(img, 'r')
        metadata = cls.__make_metadata(os.path.basename(img), big_img.size)
        frames = cls.__make_frames(info)
        return {'frames': frames, 'metadata': metadata}

    @classmethod
    def __make_metadata(cls, name, size):
        return {
            'format': 3,
            'premultiplyAlpha': False,
            'pixelFormat': 'RGBA8888',
            'realTextureFileName': name,
            'smartupdate': '$TexturePacker:SmartUpdate:%s$' % ':'.join(['f' * 32] * 3),
            'textureFileName': name,
            'size': cls.__convert_point(size)
        }

    @classmethod
    def __make_frames(cls, info):
        frames = {}
        for k, v in info.iteritems():
            frame = cls.__make_frame(v)
            k = str(k).decode('utf-8')
            frames[k + '.png'] = frame
        return frames

    @classmethod
    def __make_frame(cls, rect):
        size = cls.__convert_point(rect[2:4])
        return {
            'spriteSize': size,
            'textureRotated': False,
            'spriteOffset': '{0,0}',
            'textureRect': cls.__convert_rect(rect),
            'aliases': [],
            'spriteSourceSize': size
        }

    @classmethod
    def __convert_rect(cls, rect):
        return '{{%d,%d},{%d,%d}}' % (rect[0], rect[1], rect[2], rect[3])

    @classmethod
    def __convert_point(cls, pt):
        return '{%d,%d}' % (pt[0], pt[1])


